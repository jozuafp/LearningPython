#Coba Operator 2
a = int(input("Inputkan nilai a: ")) 
b = int(input("Inputkan nilai b: ")) 
 
# apakah a sama dengan b? 
c = a == b 
print ("Apakah" ,a, "==" ,b, ":",c) 
 
# apakah a < b? 
c = a < b 
print ("Apakah" ,a, "<" ,b, ":",c) 
 
# apakah a > b?
c = a > b 
print ("Apakah" ,a, ">" ,b, ":",c) 
 
# apakah a <= b? 
c = a <= b 
print ("Apakah" ,a, "<=" ,b, ":",c) 
 
# apakah a >= b? 
c = a >= b 
print ("Apakah" ,a, ">=" ,b, ":",c) 
 
# apakah a != b? 
c = a != b 
print ("Apakah" ,a, "!=" ,b, ":",c) 